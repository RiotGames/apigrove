package com.alu.e3.common.caching;

public class Auth {
	protected String apiId, authK, userName, password;

	public Auth() {
	}
	
	public Auth(String apiId, String authK, String userName, String password) {
		super();
		this.apiId = apiId;
		this.authK = authK;
		this.userName = userName;
		this.password = password;
	}

	public String getApiId() {
		return apiId;
	}

	public void setApiId(String apiId) {
		this.apiId = apiId;
	}

	public String getAuthK() {
		return authK;
	}

	public void setAuthK(String authK) {
		this.authK = authK;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
}
